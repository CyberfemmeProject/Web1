var toggle = "off";

pin.onclick = function(){
  if(toggle == "off"){
    pin.setAttribute("src","map.png");
    toggle = "on";
    testMusic.play();
  }else {
    pin.setAttribute
    ("src","https://pngriver.com/wp-content/uploads/2018/04/Download-Pin-Map-Church-Logo-Png-Image-96327-For-Designing-Projects.png");
    toggle = "off";
    testMusic.pause();
  }

}
